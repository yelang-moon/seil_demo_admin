/**
 * AI 챗봇용 컨텍스트 지식 (질문 키워드 기반)
 */
export function getRelevantKnowledge(question: string): string {
  const q = question.toLowerCase()
  const sections: string[] = []

  // 항상 포함: SEIL 제품 + 전략 인사이트
  sections.push(SEIL_PRODUCTS)
  sections.push(STRATEGIC_INSIGHTS)

  // 키워드 기반 추가 지식
  if (q.includes('경쟁') || q.includes('competitor') || q.includes('업체') || q.includes('기업') || q.includes('회사')) {
    sections.push(GLOBAL_COMPETITORS)
  }
  if (q.includes('시장') || q.includes('규모') || q.includes('전망') || q.includes('market') || q.includes('성장')) {
    sections.push(MARKET_DATA)
  }
  if (q.includes('규제') || q.includes('법') || q.includes('정책') || q.includes('금지') || q.includes('환경')) {
    sections.push(REGULATIONS)
  }
  if (q.includes('원자재') || q.includes('원가') || q.includes('가격') || q.includes('pp') || q.includes('pet') || q.includes('pla') || q.includes('소재')) {
    sections.push(RAW_MATERIALS)
  }
  if (q.includes('트렌드') || q.includes('m&a') || q.includes('합병') || q.includes('인수') || q.includes('기술')) {
    sections.push(INDUSTRY_TRENDS)
  }
  if (q.includes('설비') || q.includes('기계') || q.includes('장비') || q.includes('공정') || q.includes('성형') || q.includes('vfk')) {
    sections.push(SEIL_EQUIPMENT)
  }
  if (q.includes('해외') || q.includes('수출') || q.includes('일본') || q.includes('미국') || q.includes('동남아') || q.includes('아마존') || q.includes('글로벌')) {
    sections.push(GLOBAL_COMPETITORS)
    sections.push(REGULATIONS)
    sections.push(MARKET_DATA)
  }

  // 중복 제거
  const unique = [...new Set(sections)]
  return unique.join('\n\n---\n\n')
}

